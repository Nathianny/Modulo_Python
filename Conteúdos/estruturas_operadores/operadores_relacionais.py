"""
Operadores Relacionais
== - igual
!= - diferente
<  - menor
>  - maior
<= - menor ou igual
>= - maior ou igual
"""

# Criação de variáveis
x = 5;
y = 7;

# Maior que
print(x > y); # Esperamos False
print();

# Menor que
print(x < y); # Esperamos True
print();

# Igualdade
print(x == y); # Esperamos False
print();

# Maior ou igual a
print(x >= y); # Esperamos False
print();

# Menor ou iual a 
print(x <= y); # Esperamos True
print();

# Diferente de
print(x != y); # Esperamos True
print();